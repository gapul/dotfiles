# Advanced System Optimization Script
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
DOTFILES_DIR="$(dirname "$SCRIPT_DIR")"

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

log_info() { echo -e "${BLUE}[INFO]${NC} $1"; }
log_success() { echo -e "${GREEN}[SUCCESS]${NC} $1"; }
log_warning() { echo -e "${YELLOW}[WARNING]${NC} $1"; }
log_error() { echo -e "${RED}[ERROR]${NC} $1"; }

# Configure nix build optimization
optimize_nix_builds() {
    log_info "=== Optimizing Nix Build Configuration ==="
    
    local nix_conf="/etc/nix/nix.conf"
    
    # Backup current configuration
    if [[ -f "$nix_conf" ]]; then
        sudo cp "$nix_conf" "${nix_conf}.backup.$(date +%Y%m%d_%H%M%S)"
        log_success "Backed up existing nix.conf"
    fi
    
    # Create optimized nix.conf
    cat << 'EOF' | sudo tee "$nix_conf" > /dev/null
# Nix Configuration - Optimized for macOS
# Generated by dotfiles system-optimization.sh

# Build optimization
max-jobs = auto
cores = 0
parallel-shell-jobs = 4

# Binary caches
substituters = https://cache.nixos.org/ https://nix-community.cachix.org
trusted-public-keys = cache.nixos.org-1:6NCHdD59X431o0gWypbMrAURkbJ16ZPMQFGspcDShjY= nix-community.cachix.org-1:mB9FSh9qf2dCimDSUo8Zy7bkq5CX+/rkCWyvRCYg3Fs=

# Store optimization
auto-optimise-store = true
min-free = 1073741824    # 1GB
max-free = 5368709120    # 5GB

# Build features
system-features = nixos-test benchmark big-parallel kvm

# Experimental features
experimental-features = nix-command flakes

# Keep outputs and derivations for development
keep-outputs = true
keep-derivations = true

# Sandbox builds (security)
sandbox = true
restrict-eval = true

# Logging
log-lines = 50
show-trace = true
EOF

    log_success "Optimized nix.conf configuration"
    
    # Restart nix-daemon to apply changes
    log_info "Restarting nix-daemon..."
    sudo launchctl unload /Library/LaunchDaemons/org.nixos.nix-daemon.plist 2>/dev/null || true
    sudo launchctl load /Library/LaunchDaemons/org.nixos.nix-daemon.plist
    log_success "Nix daemon restarted"
}

# Create automated maintenance launchd job
create_maintenance_schedule() {
    log_info "=== Setting up automated maintenance ==="
    
    local plist_file="$HOME/Library/LaunchAgents/com.dotfiles.maintenance.plist"
    
    # Create LaunchAgent for weekly maintenance
    cat << EOF > "$plist_file"
