}

# Create system performance monitoring
setup_performance_monitoring() {
    log_info "=== Setting up performance monitoring ==="
    
    local monitor_script="$DOTFILES_DIR/scripts/performance-monitor.sh"
    
    cat << 'EOF' > "$monitor_script"
#!/bin/bash

# Performance Monitoring Script
set -euo pipefail

METRICS_DIR="$HOME/.dotfiles/metrics"
mkdir -p "$METRICS_DIR"

# Generate timestamp
TIMESTAMP=$(date +%Y%m%d_%H%M%S)

# System metrics
{
    echo "=== System Performance Report - $(date) ==="
    echo ""
    
    echo "## Memory Usage"
    vm_stat | head -10
    echo ""
    
    echo "## Disk Usage"
    df -h | head -5
    echo ""
    
    echo "## Nix Store Size"
    if command -v nix >/dev/null 2>&1; then
        nix store optimise --dry-run 2>/dev/null | tail -3 || echo "Nix store analysis unavailable"
    fi
    echo ""
    
    echo "## Top Processes"
    ps aux | head -10
    echo ""
    
    echo "## Load Average"
    uptime
    echo ""
    
} > "$METRICS_DIR/performance-$TIMESTAMP.log"

# Keep only last 30 days of metrics
find "$METRICS_DIR" -name "performance-*.log" -mtime +30 -delete 2>/dev/null || true

echo "Performance metrics saved to: $METRICS_DIR/performance-$TIMESTAMP.log"
EOF

    chmod +x "$monitor_script"
    log_success "Performance monitoring script created"
}

# Create automated security updates
setup_security_automation() {
    log_info "=== Setting up security automation ==="
    
    local security_script="$DOTFILES_DIR/scripts/security-updater.sh"
    
    cat << 'EOF' > "$security_script"
#!/bin/bash

# Automated Security Updates Script
set -euo pipefail

DOTFILES_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
LOG_FILE="$HOME/Library/Logs/dotfiles-security-updates.log"

log_with_timestamp() {
    echo "[$(date '+%Y-%m-%d %H:%M:%S')] $1" | tee -a "$LOG_FILE"
}

# Update flake.lock
update_flake_lock() {
    log_with_timestamp "Updating flake.lock..."
    cd "$DOTFILES_DIR/nix"
    
    if nix flake update; then
        log_with_timestamp "Successfully updated flake.lock"
        
        # Check if there are any changes
        if git diff --quiet flake.lock; then
            log_with_timestamp "No updates available"
        else
            log_with_timestamp "Changes detected in flake.lock"
            
            # Commit changes
            git add flake.lock
            git commit -m "chore: automated security update of flake.lock

🤖 Automated security update
$(date '+%Y-%m-%d %H:%M:%S')

Co-Authored-By: Security Automation <security@dotfiles.local>"
            
            log_with_timestamp "Committed flake.lock updates"
        fi
    else
        log_with_timestamp "ERROR: Failed to update flake.lock"
        return 1
    fi
}

# Run security checks
run_security_checks() {
    log_with_timestamp "Running security analysis..."
    
    if "$DOTFILES_DIR/scripts/enhanced-dependency-check.sh" security > /tmp/security-check.log 2>&1; then
        log_with_timestamp "Security check completed successfully"
    else
        log_with_timestamp "WARNING: Security check found issues - see /tmp/security-check.log"
    fi
}

# Main execution
main() {
    log_with_timestamp "=== Starting automated security update ==="
    
    update_flake_lock
    run_security_checks
    
    log_with_timestamp "=== Security update completed ==="
}

main "$@"
